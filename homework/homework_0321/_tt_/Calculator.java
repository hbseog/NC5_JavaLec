package homework_0321._tt_;

class Calculator {
	
	class Add {
		int add(int a, int b) {
			return a+b;
		}
	}
	
	class Sub {
		int sub(int a, int b) {
			return a - b;
		}
	}
	
	 class Mul {
		int mul(int a, int b) {
			return a * b;
		}
	}
	
	class Div {
		int div(int a, int b) {
			return a / b;
		}
	}
	
}